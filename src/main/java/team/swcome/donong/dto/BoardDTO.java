package team.swcome.donong.dto;

import javax.annotation.Resource;

@Resource
public class BoardDTO {
	int id;
	String name;
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
